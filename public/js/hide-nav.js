function showNav() {
    var nav2 = document.getElementById("nav-2")
    var closeBtn = document.getElementById("close-btn")
    var burgerBtn = document.getElementById("burger-btn")

    nav2.classList.toggle("show-nav-2");
    closeBtn.classList.toggle("hide-btn");
    burgerBtn.classList.toggle("hide-btn");
}

function hideNav() {
    var nav2 = document.getElementById("nav-2")
    var closeBtn = document.getElementById("close-btn")
    var burgerBtn = document.getElementById("burger-btn")

    nav2.classList.toggle("show-nav-2");
    closeBtn.classList.toggle("hide-btn");
    burgerBtn.classList.toggle("hide-btn");
}

